package com.javaex.re04;

public class Ex01 {

	public static void main(String[] args) {
		
		// 초기값 증감식 조건식
		
		int i = 0;
		
		while(i<5) {
			System.out.println(i);
			System.out.println("I Like Java "+ i);
			
			i++;
		}

		/*  i<5 i==0일때 i++이면 0<5 이기 때문에 증감중~~~
		 *  i<5 i==1일때 i++이면 1<5 이기 때문에 증감중~~~
		 *  i<5 i==2일때 i++이면 2<5 이기 때문에 증감중~~~
		 *  i<5 i==3일때 i++이면 3<5 이기 때문에 증감중~~~
		 *  i<5 i==4일때 i++이면 4<5 이기 때문에 증감중~~~
		 *  i<5 i==5일때 i++이면 5<5 는 아니기 때문에 종료.
		 */
		
}}
